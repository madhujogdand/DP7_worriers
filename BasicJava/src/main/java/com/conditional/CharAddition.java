package com.conditional;

public class CharAddition {

	public static void main(String args[]) {
		
		char c1=65;
		char c2=2;
		
		char c3=(char)(c1+c2);
		
		System.out.println(c3);
		
		int i1=97;
		int i3=3;
		
		char ch=(char)(i1+i3);
		System.out.println(ch);
		
		char ch1='0';
		char ch2='5';
		
		char result=(char)(ch1+ch2);
		System.out.println(result);
		
	}
}
