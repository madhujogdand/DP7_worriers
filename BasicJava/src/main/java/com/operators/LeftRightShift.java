package com.operators;

public class LeftRightShift {

	  public static void main(String args[]) {
		 
		  // LeftShift
		  
		  System.out.println(10<<2);
		  System.out.println(10<<3);
		  
		  // RightShift
		  
		  System.out.println(10>>1);
		  System.out.println(10>>2);
	  }
		  
	  }

	  

