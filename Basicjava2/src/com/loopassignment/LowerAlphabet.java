package com.loopassignment;

public class LowerAlphabet {
	
	public static void main (String args[]) {
		
		System.out.println("------------Lower Alphabets----------------");
		for(char i=97;i<=122;i++)
		{
			System.out.print(i+" ");
		}
		System.out.println();
		
		System.out.println("------------Capital Alphabets----------------");
		
		for(char i=65;i<=90;i++)
		{
			System.out.print(i+" ");
		}
	}

}
